'''DAY 7 - Using arithmetic operators to create calculator which can calculate basic operations b/w
two numbers (not input) and display result in human readable format'''
a = 5
b = 11
print(a, "+", b, "=", a+b)
print(a, "-", b, "=", a-b)
print(a, "x", b, "=", a*b)
print(a, "divided by", b, "=", a/b)
print(a, "to the power of", b, "=", a**b)
print("Quotient when", a, "is divided by", b, "=", a//b)
print("Remainder when", a, "is divided by", b, "=", a%b)